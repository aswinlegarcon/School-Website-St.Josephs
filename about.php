function reveal() {
  var reveals = document.querySelectorAll('.abt-carousel-reveal,.ab-1,.ab-2,.ab-3,.ab-4,.reveal-diary,.reveal-abt-card');
  var windowHeight = window.innerHeight;
  var revealPoint = 150;

  reveals.forEach(function(revealElement) {
    var revealTop = revealElement.getBoundingClientRect().top;

    if (revealTop < windowHeight - revealPoint) {
      revealElement.classList.add('active');
    } else {
      revealElement.classList.remove('active');
    }
  });
}
