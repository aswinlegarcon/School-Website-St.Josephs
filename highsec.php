  <script>
    

window.addEventListener('DOMContentLoaded', reveal);
window.addEventListener('scroll', reveal);
function reveal() {
  var reveals = document.querySelectorAll('.infra-new-reveal,.highsec-carousel-reveal,.infrastructure-text-reveal');
  var windowHeight = window.innerHeight;
  var revealPoint = 120;

  reveals.forEach(function(revealElement) {
    var revealTop = revealElement.getBoundingClientRect().top;

    if (revealTop < windowHeight - revealPoint) {
      revealElement.classList.add('active');
    } else {
      revealElement.classList.remove('active');
    }
  });
}
  </script>

